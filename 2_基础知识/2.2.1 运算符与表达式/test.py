# 总计24种：先12个整除//（全竖排）→ 再12个取模%（全竖排），同序列一一对应，不交叉
print("="*45 + "  第一板块：整除运算 // （12个，全竖排）  " + "="*45)
# 1. 7&3 大÷小 4种
print("【1. 7与3 大÷小】")
div1 = ["7 // 3", "7 // -3", "-7 // 3", "-7 // -3"]
for expr in div1:
    print(f"{expr:>12}  →  {eval(expr)}")
# 2. 7&3 小÷大 4种
print("\n【2. 3与7 小÷大】")
div2 = ["3 // 7", "3 // -7", "-3 // 7", "-3 // -7"]
for expr in div2:
    print(f"{expr:>12}  →  {eval(expr)}")
# 3. 5&5 相等互除 4种
